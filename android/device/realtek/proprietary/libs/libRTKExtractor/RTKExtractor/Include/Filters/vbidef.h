//Teletext
#define PDC_PIL_TIMER     0x7fff
#define PDC_PIL_STOP      0x7fBf
#define PDC_PIL_PAUSE     0x7f7f
#define PDC_PIL_RESUME    0x7f3f
#define PDC_PIL_NONE      0xffff

#define PDC_LCI(info)   ((info >>  6) & 0x03)
#define PDC_LUF(info)   ((info >>  5) & 0x01)
#define PDC_PRF(info)   ((info >>  4) & 0x01)
#define PDC_PCS(info)   ((info >>  2) & 0x03)
#define PDC_MI(info)    ((info >>  1) & 0x01)

#define PDC_CI(cni)     ((cni  >>  8) & 0xff)
#define PDC_NI(cni)     ((cni  >>  0) & 0xff)

#define PDC_DAY(pil)    ((pil  >> 15) & 0x1f)
#define PDC_MON(pil)    ((pil  >> 11) & 0x0f)
#define PDC_HOUR(pil)   ((pil  >>  6) & 0x1f)
#define PDC_MIN(pil)    ((pil  >>  0) & 0x3f)

